package main

import "fmt"

func main() {
	a := 20
	b := 10

	// suma
	result := a + b
	fmt.Println("Suma: ", result)

	result = a - b
	fmt.Println("Resta: ", result)

	result = a * b
	fmt.Println("multipl: ", result)

	result = a / b
	fmt.Println("divi: ", result)

	result = a % b
	fmt.Println("modulo: ", result)

	// para ponerlo con decimales la divconst

	div := 3.0 / 2.0
	fmt.Println("Divi decimal: ", div)

}
